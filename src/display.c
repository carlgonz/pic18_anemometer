void DispDrawFan(uint8_t frame)
{
    if(frame)
        for(uint8_t j=0; j<3; j++)
        {
            LcdSetColRow(FAN_COL,j);
            for(uint8_t i=0; i<19; i++)
                LcdWriteData(fan1[j][i],70);
        }
    else
        for(uint8_t j=0; j<3; j++)
        {
            LcdSetColRow(FAN_COL,j);
            for(uint8_t i=0; i<19; i++)
                LcdWriteData(fan0[j][i],70);
        }
}

void DispDrawFrame(void)
{
	for(uint8_t i=0; i<8; i++)
	{
		LcdSetColRow(0, i); /* Start point left */
		LcdSetColRow(64, i);/* Start point right */
		for(uint8_t j=0; j<128; j++)
        {
            uint8_t tmp = frame[i][j];
			LcdWriteData(tmp,j);
        }
	}
}

void DispDrawBat(uint8_t bar)
{
	if (bar > 4) return;
	
	LcdSetColRow(BAT_START, 1); /* Start point left */
	for(uint8_t i=0; i< bar; i++)
	{
		/* Draw 1 full bar */
		LcdWriteData(254, BAT_START);
		LcdWriteData(254, BAT_START);
		LcdWriteData(254, BAT_START);
		LcdWriteData(0, BAT_START);
	}
	
	LcdSetColRow(BAT_START + bar*4, 1); /* Start point left */
	for(uint8_t i=0; i< 4-bar; i++)
	{
		/* Draw 1 empty bar */
		LcdWriteData(0, BAT_START);
		LcdWriteData(0, BAT_START);
		LcdWriteData(0, BAT_START);
		LcdWriteData(0, BAT_START);
	}
	
}

static uint8_t current_number[5]={UINT8_MAX, UINT8_MAX, UINT8_MAX, UINT8_MAX, UINT8_MAX};
void DispDrawDigit(uint8_t num, uint8_t pos)
{
	if(num > 9) return;
	
	if(pos < 5)
    {
        if(current_number[pos] == num ) return; /* Not draw repeated number*/
        current_number[pos] = num;
        pos = digit_pos[pos];
    }
	else if( (pos != POS1) || (pos != POS2) || (pos != POS3) || (pos != POS4) || (pos != POS5)) return;
	uint8_t tmp;
	
	for(uint8_t i=0; i<4; i++)
	{
		LcdSetColRow(pos, i+4);
		if(pos == POS3) /* Set xy in right too */
			LcdSetColRow(64, i+4);
			
		for(uint8_t j=0; j<20; j++)
		{
                    switch(num)
                    {
                        case 0:
                            tmp = num0[i][j];
                            break;
                        case 1:
                            tmp = num1[i][j];
                            break;
                        case 2:
                            tmp = num2[i][j];
                            break;
                        case 3:
                            tmp = num3[i][j];
                            break;
                        case 4:
                            tmp = num4[i][j];
                            break;
                        case 5:
                            tmp = num5[i][j];
                            break;
                        case 6:
                            tmp = num6[i][j];
                            break;
                        case 7:
                            tmp = num7[i][j];
                            break;
                        case 8:
                            tmp = num8[i][j];
                            break;
                        case 9:
                            tmp = num9[i][j];
                            break;
                        default:
                            break;
                    }
			//tmp = disp_number[num][i][j];
			LcdWriteData(tmp, pos+j);
		}
	}
}

void DispDrawNumber(uint16_t number)
{
    /* ASSERT NUMBER MAX 16 BITS */

    uint8_t p4 = (number % 10);
    uint8_t p3 = (number % 100) / 10;
    uint8_t p2 = (number % 1000) / 100;
    uint8_t p1 = (number % 10000) / 1000;
    uint8_t p0 = (number % 100000) / 10000;

    DispDrawDigit(p4, 4);
    DispDrawDigit(p3, 3);
    DispDrawDigit(p2, 2);
    DispDrawDigit(p1, 1);
    DispDrawDigit(p0, 0);
}

